package pl.lehmann.lab5;

import android.content.Intent;
import android.os.Bundle;
import android.support.v4.app.FragmentTransaction;
import android.support.v7.app.AppCompatActivity;
import android.view.View;

public class MainActivity extends AppCompatActivity implements NetflixListFragment.Listener {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
    }

    @Override
    public void itemClicked(long id) {
        View fragmentContainer = findViewById(R.id.fragment_container);
        if (fragmentContainer != null) {
            NetflixFragment details = new NetflixFragment();
            FragmentTransaction ft = getSupportFragmentManager().beginTransaction();
            details.setNetflix(id);
            ft.replace(R.id.fragment_container, details);
            ft.setTransition(FragmentTransaction.TRANSIT_FRAGMENT_FADE);
            ft.addToBackStack(null);
            ft.commit();
        } else {
            Intent intent = new Intent(this, NetflixActivity.class);
            intent.putExtra(NetflixActivity.EXTRA_NETFLIX_ID, (int) id);
            startActivity(intent);
        }
    }
}
